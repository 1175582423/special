package com.zhixingbus.server.bean.poseidon;

import com.jfinal.plugin.activerecord.Model;

/**
 * 线路纠错模型
 * 
 * @author chenxi CREATE TABLE `tb_error_correction` ( `id` varchar(255) NOT
 *         NULL, `userId` varchar(255) DEFAULT NULL, `road` varchar(255) NOT
 *         NULL, `direction` varchar(255) NOT NULL, `cityId` int(11) NOT NULL,
 *         `errorContent` varchar(500) NOT NULL, `contact` varchar(50) DEFAULT
 *         NULL, `longitude` double NOT NULL, `laititude` double NOT NULL,
 *         `updateTime` varchar(255) NOT NULL, `status` int(11) NOT NULL,
 *         PRIMARY KEY (`id`) ) ENGINE=InnoDB DEFAULT CHARSET=utf8;
 */
public class ErrorCorrectionBean extends Model<ErrorCorrectionBean> {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String id;
	private String userId;
	private String road;
	private String direction;
	private int cityId;
	private String errorContent;
	private String contact;
	private double longitude;
	private double laititude;
	private String updateTime;
	private int status;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getRoad() {
		return road;
	}

	public void setRoad(String road) {
		this.road = road;
	}

	public String getDirection() {
		return direction;
	}

	public void setDirection(String direction) {
		this.direction = direction;
	}

	public int getCityId() {
		return cityId;
	}

	public void setCityId(int cityId) {
		this.cityId = cityId;
	}

	public String getErrorContent() {
		return errorContent;
	}

	public void setErrorContent(String errorContent) {
		this.errorContent = errorContent;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public double getLaititude() {
		return laititude;
	}

	public void setLaititude(double laititude) {
		this.laititude = laititude;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
