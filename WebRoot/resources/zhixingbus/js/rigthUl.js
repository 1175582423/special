/**
 * 显示公交车到站的实时数据显示
 */
function showMapHeadInfo(name,endStationName,startTime,endTime,price,toSationName,sationNum,m,s){
	if(endStationName){
		$('.businfo_0').html(name+"开往【"+endStationName+"】方向");
		$('.businfo_1').html("首末班车："+startTime+"--"+endTime+" <span>票价:"+price+"</span></div>");
	}
	$('.businfo_2').html("距【"+toSationName+"】站"+sationNum+"站,约" + m + "分"	+ s + "秒");
}